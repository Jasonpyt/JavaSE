package com.jerry.base;

public class Demo02 {
    public static void main(String[] args) {
        //八大基本数据类型

        //整数
        int num1 = 10;
        byte num2 = 20;
        short num3 = 30;
        long num4 = 40L;

        //小数
        float num5 = 50.1F;
        double num6 = 3.1415926;

        //字符
        char name = '刘';

        //字符串
        String str = "JerryLau";

        //布尔值
        boolean flag = true;
        boolean flag1 = false;

        System.out.println("java是强类型语言");

    }
}
